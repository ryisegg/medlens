export function searchTCMHerbs(query: string, category?: TCMCategory | 'All'): TCMHerb[] {
  const herbs = category && category !== 'All'
    ? TCM_HERBS.filter(h => h.category === category)
    : TCM_HERBS;

  if (!query.trim()) return herbs;

  const q = query.toLowerCase().trim();
  return herbs.filter(h => {
    const haystack = [
      h.nameZh, h.namePinyin, h.nameEn, h.nameLatin,
      h.category, ...h.aliases,
      ...h.functions, ...h.indications,
    ].join(' ').toLowerCase();
    return haystack.includes(q);
  });
}

export function getTCMHerbById(id: string): TCMHerb | undefined {
  return TCM_HERBS.find(h => h.id === id);
}
